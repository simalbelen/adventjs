const createXmasTree = (height) => {
    let tree = ''
    let base = height - 1
    let leafs = 1

    const iter = [...Array(height).keys()]
    iter.forEach((i) => {
        tree += '_'.repeat(base) + '*'.repeat(leafs) + '_'.repeat(base) + '\n'
        base -= 1
        leafs += 2
    })

    //Add base
    tree += '_'.repeat(height - 1) + '#' + '_'.repeat(height - 1) + '\n'
    tree += '_'.repeat(height - 1) + '#' + '_'.repeat(height - 1)

    return tree
}

